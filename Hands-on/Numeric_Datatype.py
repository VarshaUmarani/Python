# Numeric Datatypes

def main():

	# Integer Datatype
	no = 11				# int
	print("Value of no is : ",no)
	print("Data type of no is : ",type(no))
	print("Unique ID of no is : ",id(no))

	# Float Datatype
	value = 11.45		# float
	print("Value of value is : ",value)
	print("Data type of value is : ",type(value))
	print("Unique ID of value is : ",id(value))

	# Complex Datatype
	a = 4 + 5j			# complex number
	print("Value of a is : ",a)
	print("Data type of a is : ",type(a))
	print("Unique ID of a is : ",id(a))

if __name__ == "__main__":
	main()